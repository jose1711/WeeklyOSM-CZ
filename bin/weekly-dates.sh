#!/bin/bash

test "$2" == "--noswap" && {
  #01/06/2016-01/07/2016 -> 01.06. - 01.07.2016
  sedfile 's/\([0-9][0-9]\)\/\([0-9][0-9]\)\/201\([6789]\)-\([0-9][0-9]\)\/\([0-9][0-9]\)\/201\([6789]\)/\1.\2. - \4.\5.201\3/' $1

  #01/06/2016 -> 01.06.2016
  sedfile 's/>\([0-9][0-9]\)\/\([0-9][0-9]\)\/201\([6789]\)/>\1.\2.201\3/' $1

} || {
  #01/06/2016-01/07/2016 -> 06.01. - 07.01.2016
  sedfile 's/\([0-9][0-9]\)\/\([0-9][0-9]\)\/201\([6789]\)-\([0-9][0-9]\)\/\([0-9][0-9]\)\/201\([6789]\)/\2.\1. - \5.\4.201\3/' $1

  #01/06/2016 -> 06.01.2016
  sedfile 's/>\([0-9][0-9]\)\/\([0-9][0-9]\)\/201\([6789]\)/>\2.\1.201\3/' $1

}
  
#06.01.2016-07.01.2016 -> 06.01. - 07.01.2016
sedfile 's/\([0-9][0-9]\)\.\([0-9][0-9]\)\.201\([6789]\)-\([0-9][0-9]\)\.\([0-9][0-9]\)\.201\([6789]\)/\1.\2. - \4.\5.201\3/' $1


#2016-05-01 -> 01.05.2016
